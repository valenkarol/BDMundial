class Jugador:

    def __init__(
        self,
        nombre,
        posicion,
        numero,
        valor,
        edad,
        peso,
        estatura,
        id_equipo
    ):

        self.nombre = nombre
        self.posicion = posicion
        self.numero = numero
        self.valor = valor
        self.edad = edad
        self.peso = peso
        self.estatura = estatura
        self.id_equipo = id_equipo